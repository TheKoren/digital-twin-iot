package com.koren.digitaltwin.models.message.data.wifi.enums;

/**
 * Enum representing the possible modes of a Wi-Fi connection.
 */
public enum WifiMode {
    /** Unknown Wi-Fi mode. */
    UNKNOWN,

    /** Station mode for connecting to a Wi-Fi network. */
    STATION,

    /** Combination of Access Point (AP) and Station (STA) modes. */
    AP_STA
}
