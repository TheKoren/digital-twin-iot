package com.koren.digitaltwin.models.notification;

/**
 * An enumeration representing different types of notifications in the digital twin system.
 * Possible types include ERROR, WARNING, and ANALYSIS.
 */
public enum NotificationType {
    /** Represents an error notification. */
    ERROR,

    /** Represents a warning notification. */
    WARNING,

    /** Represents an analysis notification. */
    ANALYSIS
}
